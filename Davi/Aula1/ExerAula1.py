def organizar_numeros():
    resultados = {
        'pares': [],
        'impares': [],
        'positivos': [],
        'negativos': [],
        'zeros': []
    }

    QUANTIDADE_NUMEROS = 10

    for i in range(QUANTIDADE_NUMEROS):
        while True:
            try:
                numero = int(input(f'Digite o {i+1}º número: '))
                break
            except ValueError:
                print('Entrada inválida. Por favor, digite um número inteiro.')

        if numero % 2 == 0:
            resultados['pares'].append(numero)
        else:
            resultados['impares'].append(numero)

        if numero > 0:
            resultados['positivos'].append(numero)
        elif numero < 0:
            resultados['negativos'].append(numero)
        else:
            resultados['zeros'].append(numero)

    print('\n' + '=' * 40)
    print('ORGANIZAÇÃO DOS NÚMEROS NO DICIONÁRIO:')
    print('=' * 40)

    print(f'Pares: {resultados["pares"]}')
    print(f'Ímpares: {resultados["impares"]}')
    print(f'Positivos: {resultados["positivos"]}')
    print(f'Negativos: {resultados["negativos"]}')
    print(f'Zeros: {resultados["zeros"]}')
    print('=' * 40)

organizar_numeros()

