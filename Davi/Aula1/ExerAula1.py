from collections import Counter
import string

def analisar_texto():
    texto = input('Digite o texto a ser analisado: ')

    texto_minusculo = texto.lower()

    letras_apenas = ''.join(c for c in texto_minusculo if c.isalpha())

    texto_sem_pontuacao = texto_minusculo.translate(str.maketrans('', '', string.punctuation))

    total_caracteres = len(texto)

    palavras = list(filter(None, texto_sem_pontuacao.split()))
    total_palavras = len(palavras)

    palavra_mais_longa = ''
    if palavras:
        palavra_mais_longa = max(palavras, key=len)

    contagem_letras = Counter(letras_apenas)
    letra_mais_frequente = ''
    frequencia = 0

    if contagem_letras:
        letra_mais_frequente, frequencia = contagem_letras.most_common(1)[0]

    print('\n' + '=' * 40)
    print('RESULTADOS DA ANÁLISE DE TEXTO')
    print('=' * 40)
    print(f'Total de Caracteres (com espaços): {total_caracteres}')
    print(f'Total de Palavras: {total_palavras}')
    print('-' * 40)
    print(f'Palavra Mais Longa: {palavra_mais_longa} ({len(palavra_mais_longa)} letras)')
    print(f'Letra Mais Frequente: {letra_mais_frequente} ({frequencia} ocorrências)')
    print('=' * 40)

analisar_texto()