def classificar_email_spam(texto):
    palavras_gatilho = [
        'grátis', 'dinheiro fácil', 'clique aqui',
        'urgente', 'parabéns', 'ganhe', 'bônus',
        'oferta especial', 'por apenas', 'você é um vencedor',
        'sem riscos', 'liberdade financeira', 'eliminar a dívida',
        'remédio milagroso', 'compre agora', 'tempo limitado'
    ]

    texto_limpo = texto.lower()

    pontuacao_spam = 0
    palavras_encontradas = []

    for palavra in palavras_gatilho:
        if palavra in texto_limpo:
            pontuacao_spam += 1
            palavras_encontradas.append(palavra)

    LIMITE_SPAM = 3

    status = ''

    if pontuacao_spam >= LIMITE_SPAM:
        status = 'CLASSIFICADO COMO SPAM'
    else:
        status = 'CLASSIFICADO COMO NÃO-SPAM'

    print('=' * 50)
    print(f'TEXTO AVALIADO: \'{texto}\'')
    print('-' * 50)
    print(f'Palavras Gatilho Encontradas ({pontuacao_spam} pontos):')

    if palavras_encontradas:
        print(f"-> {', '.join(palavras_encontradas)}")
    else:
        print('-> Nenhuma palavra gatilho encontrada.')

    print('-' * 50)
    print(f'CLASSIFICAÇÃO FINAL: {status}')
    print('=' * 50)

    return status

