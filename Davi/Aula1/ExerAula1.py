import matplotlib.pyplot as plt

def plotar_notas_alunos():
    alunos = [
        {'Nome': 'Felipe', 'Nota': 8},
        {'Nome': 'Matheus', 'Nota': 9},
        {'Nome': 'Luana', 'Nota': 7},
        {'Nome': 'Gabriel', 'Nota': 8},
        {'Nome': 'Bianca', 'Nota': 6}
    ]

    nomes = [aluno['Nome'] for aluno in alunos]
    notas = [aluno['Nota'] for aluno in alunos]

    plt.style.use('ggplot')

    plt.figure(figsize=(10, 6))
    plt.bar(nomes, notas, color='skyblue')

    plt.title('Desempenho dos Alunos em Python')
    plt.xlabel('Nome do Aluno')
    plt.ylabel('Nota Final')

    media = sum(notas) / len(notas)
    plt.axhline(media, color='red', linestyle='--', linewidth=1, label=f'Média: {media:.2f}')

    for i, nota in enumerate(notas):
        plt.text(i, nota + 0.1, str(nota), ha='center', va='bottom')

    plt.legend()

    plt.xticks(rotation=45, ha='right')

    plt.tight_layout()
    plt.show()

plotar_notas_alunos()

